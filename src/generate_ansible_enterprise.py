# Idempotently syncs DNS records in a BIND zone file.
# Supports all record types, --ttl, and --absent for removal.
# Records already present are never touched. Hand-added records are preserved.
#
# Usage:
#   sync_dns_records.py --zone-file /var/named/example.com.zone \\
#       --record @ A 1.2.3.4 \\
#       --record mail MX "10 mail.example.com." \\
#       --record old A 1.2.3.4 --absent
#
# Prints "added N record(s): ..." or "removed N record(s): ..." to stdout
# so Ansible can detect changes via changed_when.
import argparse
import pathlib
import re
import sys


def record_exists(text, label, rtype, value):
    pattern = (
        r"(?m)^"
        + re.escape(label)
        + r"\\s+(?:\\d+\\s+)?(?:IN\\s+)?"
        + re.escape(rtype)
        + r"\\s+"
        + re.escape(value)
        + r"\\s*$"
    )
    return bool(re.search(pattern, text, re.IGNORECASE))


def remove_record(text, label, rtype, value):
    pattern = (
        r"(?m)^"
        + re.escape(label)
        + r"\\s+(?:\\d+\\s+)?(?:IN\\s+)?"
        + re.escape(rtype)
        + r"\\s+"
        + re.escape(value)
        + r"\\s*\\n?"
    )
    return re.sub(pattern, "", text, flags=re.IGNORECASE)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--zone-file", required=True)
    ap.add_argument("--record", nargs=3, metavar=("LABEL", "TYPE", "VALUE"),
                    action="append", default=[])
    ap.add_argument("--ttl", type=int, default=None)
    ap.add_argument("--absent", action="store_true")
    args = ap.parse_args()

    zone_path = pathlib.Path(args.zone_file)
    if not zone_path.exists():
        print("error: zone file not found: {}".format(zone_path), file=sys.stderr)
        sys.exit(1)

    text = zone_path.read_text(encoding="utf-8")
    added = []
    removed = []

    for label, rtype, value in args.record:
        rtype = rtype.upper()
        if args.absent:
            if record_exists(text, label, rtype, value):
                text = remove_record(text, label, rtype, value)
                removed.append("{} {} {}".format(label, rtype, value))
        else:
            if not record_exists(text, label, rtype, value):
                ttl_part = "{} ".format(args.ttl) if args.ttl else ""
                line = "{:<24s}{}IN  {:<8s}{}\\n".format(label, ttl_part, rtype, value)
                text = text.rstrip("\\n") + "\\n" + line
                added.append("{} {} -> {}".format(label, rtype, value))

    if added or removed:
        zone_path.write_text(text, encoding="utf-8")
    if added:
        print("added {} record(s): {}".format(len(added), ", ".join(added)))
    if removed:
        print("removed {} record(s): {}".format(len(removed), ", ".join(removed)))


