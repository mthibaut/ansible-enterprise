def ascii_check() -> None:
    with open(__file__, "r", encoding="utf-8") as _f:
        _f.read().encode("ascii")


def _comment_style(path: pathlib.Path) -> str:
    if path.suffix == ".json":
        return "json"
    if path.suffix == ".md":
        return "html"
    if path.suffix in {".ini", ".cfg"}:
        return ";"
    # Zone file templates (.db.j2): DNS zone files use semicolons, not hashes.
    if path.suffix == ".j2" and pathlib.Path(path.stem).suffix == ".db":
        return ";"
    # BIND named.conf templates (.conf.j2 whose stem starts with named.conf):
    # named.conf uses // for single-line comments; # is not valid.
    if path.suffix == ".j2" and path.stem.startswith("named.conf"):
        return "//"
    return "#"


def _render_notice(rel_path: str, path: pathlib.Path) -> str:
    notice_key = "protected" if rel_path in PROTECTED_FILES else "default"
    lines = GENERATED_NOTICE[notice_key]
    style = _comment_style(path)
    if style == "html":
        body = "\n".join(lines)
        return f"<!--\n{body}\n-->\n\n"
    if style == "json":
        return ""
    border = style + " " + "-" * 77
    inner = "\n".join(f"{style} {line}" for line in lines)
    return f"{border}\n{inner}\n{border}\n"


def apply_notice(rel_path: str, content: str) -> str:
    path = pathlib.Path(rel_path)
    style = _comment_style(path)
    if style == "json":
        return content
    notice = _render_notice(rel_path, path)
    if content.startswith("#!/"):
        shebang, _, rest = content.partition("\n")
        return shebang + "\n" + notice + rest
    if path.suffix in {".gitkeep", ".gitattributes"}:
        return content
    return notice + content


def _write_file(dest: pathlib.Path, rel_path: str, content: str) -> None:
    dest.parent.mkdir(parents=True, exist_ok=True)
    final = apply_notice(rel_path, content)
    dest.write_text(final, encoding="utf-8")
    if pathlib.Path(rel_path).suffix in {".sh", ".py"}:
        dest.chmod(0o755)


def compute_lock_data(build_dir: pathlib.Path) -> dict:
    source_hash = hashlib.sha256()
    for rel_path in LOCK_SOURCE_FILES:
        source_hash.update(rel_path.encode("utf-8"))
        p = SRC / rel_path
        if p.exists():
            source_hash.update(p.read_bytes())
    output_hash = hashlib.sha256()
    for rel_path in sorted(FILE_MANIFEST):
        output_hash.update(rel_path.encode("utf-8"))
        p = build_dir / rel_path
        if p.exists():
            output_hash.update(p.read_bytes())
    return {
        "source_hash": source_hash.hexdigest(),
        "output_hash": output_hash.hexdigest(),
        "generated_files": sorted(FILE_MANIFEST),
        "source_files": LOCK_SOURCE_FILES,
    }


def write_lock(build_dir: pathlib.Path) -> None:
    """Write lock to src/.generator.lock.yml, hashing build_dir output."""
    lock = compute_lock_data(build_dir)
    lock_lines = [
        "source_hash: " + lock["source_hash"],
        "output_hash: " + lock["output_hash"],
        "generated_files:",
    ]
    lock_lines.extend(f"  - {p}" for p in lock["generated_files"])
    lock_lines.append("source_files:")
    lock_lines.extend(f"  - {p}" for p in lock["source_files"])
    LOCK_FILE.write_text("\n".join(lock_lines) + "\n", encoding="utf-8")


def generate(build_dir: pathlib.Path) -> None:
    for rel_path, content in FILE_MANIFEST.items():
        if rel_path in UNMANAGED_FILES:
            continue
        _write_file(build_dir / rel_path, rel_path, content)


def _validate_prompt() -> str:
    prompt_text = PROMPT.read_text(encoding="utf-8")
    missing = [s for s in REQUIRED_PROMPT_SECTIONS if s not in prompt_text]
    if missing:
        raise SystemExit("PROMPT.md missing required sections\n" + "\n".join(missing))
    return prompt_text


def main() -> None:
    ascii_check()
    prompt_text = _validate_prompt()
    prompt_hash = hashlib.sha256(prompt_text.encode("utf-8")).hexdigest()
    # Staging dry-run to detect errors before touching build/
    if STAGING_DIR.exists():
        shutil.rmtree(STAGING_DIR)
    STAGING_DIR.mkdir(parents=True)
    generate(STAGING_DIR)
    shutil.rmtree(STAGING_DIR)
    # Real generation into build/
    generate(BUILD_DIR)
    write_lock(BUILD_DIR)
    HASH_FILE.write_text(prompt_hash + "\n", encoding="utf-8")
    _raw_version = VERSION_FILE.read_text(encoding="utf-8").strip() if VERSION_FILE.exists() else "0"
    try:
        old_version = int(_raw_version)
    except ValueError:
        # Migrate from legacy semver string (e.g. "1.4.0") to integer counter.
        old_version = 0
    VERSION_FILE.write_text(str(old_version + 1) + "\n", encoding="utf-8")
    print(f"Generated {len(FILE_MANIFEST)} files into build/")


if __name__ == "__main__":
    main()
